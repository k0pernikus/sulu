namespace Sulu\Bundle\TagBundle\DependencyInjection\Compiler;

use Symfony\Component\DependencyInjection\Compiler\CompilerPassInterface;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\DependencyInjection\Reference;

class TagManagerCompilerPass implements CompilerPassInterface
{

    /**
     * You can modify the container here before it is dumped to PHP code.
     *
     * @param ContainerBuilder $container
     *
     * @api
     */
    public function process(ContainerBuilder $container)
    {
        if ($container->hasDefinition('security.context')) {
            // add security context to tag manager only if there is a security context
            $container->getDefinition('sulu_tag.tag_manager')->addMethodCall(
                'setSecurityContext',
                array(new Reference('security.context'))
            );
        }
    }
}
